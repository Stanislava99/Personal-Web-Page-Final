let liked1=false;

let count1=37;
document.getElementById('likes-photo1').onclick=function(){
    if(!liked1) {
        count1++;
        document.getElementById('likes-photo1').innerHTML=count1;
        liked1=true;
    }
    else {
        count1--;
        document.getElementById('likes-photo1').innerHTML=count1;
        liked1=false;
    }

}
